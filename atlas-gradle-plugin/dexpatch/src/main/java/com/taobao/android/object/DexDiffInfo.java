    private static final Pattern p = Pattern.compile("Ljava/lang/|Ljava/util/|Landroid/app/|Landroid/util/");
    private Set<DexBackedClassDef> oldClasses = Collections.synchronizedSet(new HashSet<DexBackedClassDef>());
