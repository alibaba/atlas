package com.taobao.android.builder.extension;

import com.google.common.collect.Sets;
import com.taobao.android.builder.AtlasBuildContext;
import com.taobao.android.builder.extension.annotation.Config;
import org.apache.commons.lang.StringUtils;

import java.io.File;
import java.util.ArrayList;
import java.util.HashSet;
import java.util.List;
import java.util.Set;

/**
 * Created by shenghua.nish on 2016-05-17 As in the morning.
 */
public class PatchConfig {

    private String name;

    @Config(message = "appThe application name of the registered signature", order = 0, advance = false, group = "atlas_patch")
    private String appSignName;

    @Config(message = " Play andfix patch bag ", order = 1, advance = true, group = "atlas_patch")
    private boolean createAPatch = false;

    @Config(message = " Dynamic deployment patch bag ", order = 2, group = "atlas_patch")
    private boolean createTPatch = false;

    @Config(message = "Dynamic deployment does not contain a list of files", order = 3, advance = true, group = "atlas_patch")
    private Set<String> tPatchNotIncludeFiles = Sets.newHashSet();

    @Config(message = " andfix Package filtering class A list of files ", order = 4, advance = true, group = "atlas_patch")
    private File filterFile;

    @Config(message = " andfix Package filtering class tabulation ", order = 5, advance = true, group = "atlas_patch")
    private Set<String> filterClasses = new HashSet<String>();

    private boolean apForBaseFile = true;

    private File apExploredFolder;

    private File finalBasePatchFile;

    public File getHotClassListFile() {
        return hotClassListFile;
    }

    public void setHotClassListFile(File hotClassListFile) {
        this.hotClassListFile = hotClassListFile;
    }

    private File hotClassListFile;

    public Set<String> getExcludeClasses() {
        if (excludeClasses.size() > 0){
            return excludeClasses;
        }else {
            return Sets.newHashSet("Landroid/taobao/atlas/framework/FrameworkProperties;","Landroid/taobao/atlas/bundleInfo/AtlasBundleInfoGenerator;");
        }
    }

    public void setExcludeClasses(Set<String> excludeClasses) {
        this.excludeClasses = excludeClasses;
    }

    //排除的class列表
    private Set<String>excludeClasses = new HashSet<>();

    private Boolean devlopMode = false;

    private boolean onlyIncrementInMain = true;

    private boolean onlyIncrementInAwb = true;

    private boolean containMainBundleRes = true;

    private String tpatchMainBundleName = "libcom_taobao_maindex";

    private String apatchMainBundleName = "com_taobao_maindex";

    private String tpatchHistoryUrl = "/rpc/dynamicBundle/getAllPatchInfo.json";

    public String getLastPatchUrl() {
        if (StringUtils.isEmpty(AtlasBuildContext.sBuilderAdapter.tpatchHistoryUrl)) {
            return "";
        }
        return "http://" + AtlasBuildContext.sBuilderAdapter.tpatchHistoryUrl + LastPatchUrl;
    }

    public void setLastPatchUrl(String lastPatchUrl) {
        LastPatchUrl = lastPatchUrl;
    }

    private String LastPatchUrl = "/rpc/dynamicBundle/getLastPatch.json?";

    private Boolean onlyBuildModifyAwb = false;

    private String noPatchBundles = "";

    private String buildId = "";

    private boolean tpatchWriteBuildInfo = true;

    private boolean fullResValues;

    private List<String> patchVersions = new ArrayList<>();

    public PatchConfig(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isCreateAPatch() {
        return createAPatch;
    }

    public void setCreateAPatch(boolean createAPatch) {
        this.createAPatch = createAPatch;
    }

    public boolean isCreateTPatch() {
        return createTPatch;
    }

    public void setCreateTPatch(boolean createTPatch) {
        this.createTPatch = createTPatch;
    }

    public boolean isOnlyIncrementInMain() {
        return onlyIncrementInMain;
    }

    public void setOnlyIncrementInMain(boolean onlyIncrementInMain) {
        this.onlyIncrementInMain = onlyIncrementInMain;
    }

    public boolean isOnlyIncrementInAwb() {
        return onlyIncrementInAwb;
    }

    public void setOnlyIncrementInAwb(boolean onlyIncrementInAwb) {
        this.onlyIncrementInAwb = onlyIncrementInAwb;
    }

    public boolean isContainMainBundleRes() {
        return containMainBundleRes;
    }

    public void setContainMainBundleRes(boolean containMainBundleRes) {
        this.containMainBundleRes = containMainBundleRes;
    }

    public String getTpatchMainBundleName() {
        return tpatchMainBundleName;
    }

    public void setTpatchMainBundleName(String tpatchMainBundleName) {
        this.tpatchMainBundleName = tpatchMainBundleName;
    }

    public String getApatchMainBundleName() {
        return apatchMainBundleName;
    }

    public void setApatchMainBundleName(String apatchMainBundleName) {
        this.apatchMainBundleName = apatchMainBundleName;
    }

    public Set<String> gettPatchNotIncludeFiles() {
        return tPatchNotIncludeFiles;
    }

    public void settPatchNotIncludeFiles(Set<String> tPatchNotIncludeFiles) {
        this.tPatchNotIncludeFiles = tPatchNotIncludeFiles;
    }

    public File getFilterFile() {
        return filterFile;
    }

    public void setFilterFile(File filterFile) {
        this.filterFile = filterFile;
    }

    public Set<String> getFilterClasses() {
        return filterClasses;
    }

    public void setFilterClasses(Set<String> filterClasses) {
        this.filterClasses = filterClasses;
    }

    public boolean isApForBaseFile() {
        return apForBaseFile;
    }

    public void setApForBaseFile(boolean apForBaseFile) {
        this.apForBaseFile = apForBaseFile;
    }

    public File getApExploredFolder() {
        return apExploredFolder;
    }

    public void setApExploredFolder(File apExploredFolder) {
        this.apExploredFolder = apExploredFolder;
    }

    public File getFinalBasePatchFile() {
        return finalBasePatchFile;
    }

    public void setFinalBasePatchFile(File finalBasePatchFile) {
        this.finalBasePatchFile = finalBasePatchFile;
    }

    public Boolean getDevlopMode() {
        return devlopMode;
    }

    public void setDevlopMode(Boolean devlopMode) {
        this.devlopMode = devlopMode;
    }

    public Boolean getOnlyBuildModifyAwb() {
        return onlyBuildModifyAwb;
    }

    public void setOnlyBuildModifyAwb(Boolean onlyBuildModifyAwb) {
        this.onlyBuildModifyAwb = onlyBuildModifyAwb;
    }

    public String getNoPatchBundles() {
        return noPatchBundles;
    }

    public void setNoPatchBundles(String noPatchBundles) {
        this.noPatchBundles = noPatchBundles;
    }

    public String getBuildId() {
        return buildId;
    }

    public void setBuildId(String buildId) {
        this.buildId = buildId;
    }

    public boolean isTpatchWriteBuildInfo() {
        return tpatchWriteBuildInfo;
    }

    public void setTpatchWriteBuildInfo(boolean tpatchWriteBuildInfo) {
        this.tpatchWriteBuildInfo = tpatchWriteBuildInfo;
    }

    /**
     * Whether to play the historical patch, Can be achieved by -DdexPatchEnabled=true To turn on
     *
     * @return
     */
    public String getTpatchHistoryUrl() {

        if (StringUtils.isEmpty(AtlasBuildContext.sBuilderAdapter.tpatchHistoryUrl)) {
            return "";
        }
        return "http://" + AtlasBuildContext.sBuilderAdapter.tpatchHistoryUrl + tpatchHistoryUrl;
    }

    public void setTpatchHistoryUrl(String tpatchHistoryUrl) {
        this.tpatchHistoryUrl = tpatchHistoryUrl;
    }

    public String getAppSignName() {
        return appSignName;
    }

    public void setAppSignName(String appSignName) {
        this.appSignName = appSignName;
    }

    public List<String> getPatchVersions() {
        return patchVersions;
    }

    public void setPatchVersions(List<String> patchVersions) {
        this.patchVersions = patchVersions;
    }

    public boolean isFullResValues() {
        return fullResValues;
    }

    public void setFullResValues(boolean fullResValues) {
        this.fullResValues = fullResValues;
    }
}
