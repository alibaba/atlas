/**
 * AndroidThe awb's dex compilation task, for the sake of simplicity, does not take the DexTransform task and currently does not support predex and other functions
 * Created by shenghua.nish on 2016-06-07 On the morning of 10:59. < br / >
 * TODO: Increase the Awb dexPreDex support
 */
@ParallelizableTask
public class AwbDexTask extends BaseTask {

    private File outputFolder;

    private List<String> additionalParameters;

    private Boolean enableIncremental = false;

    private Collection<File> inputFiles;

    private File inputDir;

    private DexOptions dexOptions;

    private Boolean multiDexEnabled = false;

    private Boolean optimize = true;

    private File mainDexListFile;

    @OutputDirectory
    public File getOutputFolder() {
        return outputFolder;
    }

    public void setOutputFolder(File outputFolder) {
        this.outputFolder = outputFolder;
    }

    @Input
    @Optional
    public List<String> getAdditionalParameters() {
        return additionalParameters;
    }

    public void setAdditionalParameters(List<String> additionalParameters) {
        this.additionalParameters = additionalParameters;
    }

    @Input
    public Boolean getEnableIncremental() {
        return enableIncremental;
    }

    public void setEnableIncremental(boolean enableIncremental) {
        this.enableIncremental = enableIncremental;
    }

    @InputFiles
    @Optional
    public Collection<File> getInputFiles() {
        return inputFiles;
    }

    public void setInputFiles(Collection<File> inputFiles) {
        this.inputFiles = inputFiles;
    }

    @InputDirectory
    @Optional
    public File getInputDir() {
        return inputDir;
    }

    public void setInputDir(File inputDir) {
        this.inputDir = inputDir;
    }

    @Nested
    public DexOptions getDexOptions() {
        return dexOptions;
    }

    public void setDexOptions(DexOptions dexOptions) {
        this.dexOptions = dexOptions;
    }

    @Input
    public Boolean getMultiDexEnabled() {
        return multiDexEnabled;
    }

    public void setMultiDexEnabled(boolean multiDexEnabled) {
        this.multiDexEnabled = multiDexEnabled;
    }

    @Input
    public Boolean getOptimize() {
        return optimize;
    }

    public void setOptimize(boolean optimize) {
        this.optimize = optimize;
    }

    @InputFile
    @Optional
    public File getMainDexListFile() {
        return mainDexListFile;
    }

    public void setMainDexListFile(File mainDexListFile) {
        this.mainDexListFile = mainDexListFile;
    }

    /**
     * Actual entry point for the action.
     * Calls out to the doTaskAction as needed.
     */
    @TaskAction
    public void taskAction() throws InterruptedException, ProcessException, IOException {
        Collection<File> _inputFiles = getInputFiles();
        File _inputDir = getInputDir();
        if (_inputFiles == null && _inputDir == null) {
            throw new StopExecutionException("Dex task " +
                                                 getName() +
                                                 ": inputDir and inputFiles cannot both be null");
        }
        doTaskAction(_inputFiles, _inputDir, false);
    }

    private void doTaskAction(@Nullable Collection<File> inputFiles,
                              @Nullable File inputDir,
                              boolean incremental) throws InterruptedException, ProcessException, IOException {

        File outFolder = getOutputFolder();
        if (!incremental) {
            emptyFolder(outFolder);
        }

        // if some of our .jar input files exist, just reset the inputDir to null
        for (File inputFile : inputFiles) {
            if (inputFile.exists()) {
                inputDir = null;
            }
        }
        if (inputDir != null) {
            inputFiles = getProject().files(inputDir).getFiles();
        }
        ProcessOutputHandler outputHandler = new ParsingProcessOutputHandler(new ToolOutputParser(
            new DexParser(),
            Message.Kind.ERROR,
            getILogger()),
                                                                             new ToolOutputParser(
                                                                                 new DexParser(),
                                                                                 getILogger()),
                                                                             getBuilder().getErrorReporter());

        getBuilder().convertByteCode(inputFiles,
                                     getOutputFolder(),
                                     getMultiDexEnabled(),
                                     getMainDexListFile(),
                                     getDexOptions(),
                                     outputHandler);
    }

    protected void emptyFolder(File folder) throws IOException {
        FileUtils.deletePath(folder);
        folder.mkdirs();
    }

    /**
     * Internal classes for task configuration
     */
    public static class ConfigAction extends MtlBaseTaskAction<AwbDexTask> {

        private final VariantScope scope;

        private final String awbName;

        private final AndroidConfig androidConfig;

        private final LibVariantContext variantContext;

        public ConfigAction(LibVariantContext variantContext,
                            BaseVariantOutputData baseVariantOutputData) {
            super(variantContext, baseVariantOutputData);
            this.scope = variantContext.getScope();
            this.awbName = variantContext.getAwbBundle().getName();
            this.androidConfig = variantContext.getAppExtension();
            this.variantContext = variantContext;
        }

        @Override
        public String getName() {
            return scope.getTaskName("awoDex", "[" + awbName + "]");
        }

        @Override
        public Class<AwbDexTask> getType() {
            return AwbDexTask.class;
        }

        @Override
        public void execute(AwbDexTask task) {
            final GradleVariantConfiguration config = variantContext.getVariantConfiguration();

            LibVariantContext libVariantContext = (LibVariantContext)variantContext;
            //                libVariantContext.setAwbDexTask(task);
            task.setOutputFolder(libVariantContext.getDexFolder());

            task.setDexOptions(androidConfig.getDexOptions());
            task.setAndroidBuilder(scope.getGlobalScope().getAndroidBuilder());
            task.setEnableIncremental(false);
            task.setMultiDexEnabled(false);
            task.setOptimize(true);

            ConventionMappingHelper.map(task, "inputFiles", new Callable<Collection<File>>() {

                @Override
                public Collection<File> call() throws Exception {

                    List<File> fileList = new ArrayList<File>();
                    fileList.addAll(variantContext.getAwbBundle().getLibraryJars());
                    File dir = variantContext.getJavaCDir();
                    if (null != dir) {
                        fileList.add(dir);
                    }

                    return fileList;
                }
            });

            // task.setInputFiles(awbTransform.getInputLibraries());

            task.setVariantName(scope.getVariantData().getName());
        }
    }
}
