package com.android.build.gradle.internal.api;

import com.android.build.gradle.BaseExtension;
import com.android.build.gradle.internal.core.GradleVariantConfiguration;
import com.android.build.gradle.internal.dependency.VariantDependencies;
import com.android.build.gradle.internal.pipeline.InjectTransformManager;
import com.android.build.gradle.internal.scope.VariantScope;
import com.android.build.gradle.internal.variant.BaseVariantData;
import com.android.build.gradle.internal.variant.BaseVariantOutputData;
import com.android.builder.signing.DefaultSigningConfig;
import com.android.utils.FileUtils;
import com.android.utils.StringHelper;
import com.taobao.android.builder.dependency.model.AwbBundle;
import com.taobao.android.builder.extension.AtlasExtension;
import com.taobao.android.builder.extension.TBuildType;
import org.gradle.api.Project;

import java.io.File;
import java.lang.reflect.Method;
import java.util.List;

/**
 * Created by shenghua.nish on 2016-05-19 Whether in the afternoon.
 */
public class VariantContext<T extends BaseVariantImpl, Z extends BaseExtension, E extends AtlasExtension> {

    protected T baseVariant;
    protected BaseVariantData baseVariantData;
    protected VariantDependencies variantDependency;
    protected GradleVariantConfiguration variantConfiguration;
    protected VariantScope scope;
    protected Project project;
    protected InjectTransformManager injectTransformManager;
    protected E atlasExtension;
    protected Z appExtension;

    public ApContext apContext = new ApContext();

    public VariantContext(T baseVariant, Project project, E atlasExtension, Z appExtension) {
        this.baseVariant = baseVariant;

        //classloader It could be father-son relationship, protected The direct invocation of the method throws an exception
        try {
            Class clazz = baseVariant.getClass();
            Method method = clazz.getDeclaredMethod("getVariantData");
            method.setAccessible(true);
            this.baseVariantData = (BaseVariantData) method.invoke(baseVariant);
//            this.baseVariantData = baseVariant.getVariantData();
        } catch (Throwable e) {
            e.printStackTrace();
        }

        this.variantDependency = this.baseVariantData.getVariantDependency();

        this.scope = this.baseVariantData.getScope();

        this.variantConfiguration = this.baseVariantData.getVariantConfiguration();
        this.project = project;
        this.injectTransformManager = new InjectTransformManager(project, variantConfiguration.getFullName());
        this.atlasExtension = atlasExtension;
        this.appExtension = appExtension;

    }

    public T getBaseVariant() {
        return baseVariant;
    }

    public BaseVariantData getBaseVariantData() {
        return baseVariantData;
    }

    public VariantDependencies getVariantDependency() {
        return variantDependency;
    }

    public VariantScope getScope() {
        return scope;
    }

    public GradleVariantConfiguration getVariantConfiguration() {
        return variantConfiguration;
    }

    public Project getProject() {
        return project;
    }

    public InjectTransformManager getInjectTransformManager() {
        return injectTransformManager;
    }

    public String getVariantName() {
        return variantConfiguration.getFullName();
    }

    public AtlasExtension getAtlasExtension() {
        return atlasExtension;
    }

    public Z getAppExtension() {
        return appExtension;
    }

    public TBuildType getBuildType() {
        TBuildType tBuildType = (TBuildType) atlasExtension.getBuildTypes().findByName(variantConfiguration.getBuildType().getName());
        if (null == tBuildType) {
            tBuildType = new TBuildType(variantConfiguration.getBuildType().getName());
        }
        return tBuildType;
    }

    public DefaultSigningConfig getSigningConfig() {
        TBuildType tBuildType = getBuildType();
        return tBuildType.getSigningConfig();
    }

    public List<BaseVariantOutputData> getVariantOutputData() {
        return baseVariantData.getOutputs();
    }


    public File getMergeAssets(AwbBundle awbBundle) {
        return new File(scope.getGlobalScope().getIntermediatesDir(),
                "/awb-assets/" + variantConfiguration.getFullName() + "/"
                        + awbBundle.getName());
    }

    public File getMergeResources(AwbBundle awbBundle) {
        return new File(scope.getGlobalScope().getIntermediatesDir(),
                "/awb-res/merged/" + variantConfiguration.getFullName() + "/" + awbBundle.getName());
    }

    public File getPngsOutputDir(AwbBundle awbBundle) {
        return FileUtils.join(new File(scope.getGlobalScope().getGeneratedDir().getParentFile(),
                        "awb-generated"),
                StringHelper.toStrings("res", "pngs", scope.getDirectorySegments(),
                        awbBundle.getName()));
    }


}
