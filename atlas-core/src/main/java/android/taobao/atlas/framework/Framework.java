    static boolean restoreBundle(String[] locations) {
        try {
            for (String location : locations) {
                if (isKernalBundle(location)) {
                    final File bundleDir = new File(STORAGE_LOCATION, KernalBundle.KERNAL_BUNDLE_NAME);
                    if (!bundleDir.exists()) {
                        return false;
                    } else {
                        if (!KernalBundle.downgradeRevision(bundleDir, false)) {
                            return false;
                        }
                    }
                } else {
                    final File bundleDir = new File(STORAGE_LOCATION, location);
                    if (!bundleDir.exists()) {
                        return false;
                    } else {
                        if (!BundleArchive.downgradeRevision(location, bundleDir, false)) {
                            return false;
                        }
                    }
                }
            }
        } catch (Exception e) {
            return false;
        }
        return true;
    }


