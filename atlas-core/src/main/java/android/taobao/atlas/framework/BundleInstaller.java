    void release(){
        mLocation = null;
        mBundleSourceInputStream = null;
        mBundleSourceFile = null;
        mListener = null;
        mTmpBundleSourceFile = null;
        mTmpBundleSourceInputStream = null;
        mTransitive = false;
    }

