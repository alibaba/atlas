    public void installTransitivelyAsync(String[] location, InstallListener listener){
        if(location!=null){
            release();
            mTransitive = true;
            mLocation = location;
            mListener = listener;
            try {
                installBundleInternal(false);
            }catch(Throwable e){
                e.printStackTrace();
                if(mListener!=null){
                    mListener.onFinished();
                }
                BundleInstallerFetcher.recycle(this);
            }
        }
    }

