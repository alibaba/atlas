    public void installAsync(final String[] location, final InputStream[] input, InstallListener listener){
        if(Thread.currentThread().getId() != Looper.getMainLooper().getThread().getId()){
            throw new RuntimeException("method executeOnUIThread is only prepared for UI Thread");
        }
        release();
        mListener = listener;
        mBundleSourceInputStream = input;
        mLocation = location;
        try {
            checkSrc(location,input);
            installBundleInternal(false);
        }catch(Throwable e){
            e.printStackTrace();
            if(mListener!=null){
                mListener.onFinished();
            }
            BundleInstallerFetcher.recycle(this);
        }
    }

