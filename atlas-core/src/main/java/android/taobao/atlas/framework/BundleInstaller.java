    public synchronized static void createIdleInstallerIfNeed(){
        if(sIdleHandler==null){
            sIdleHandler = new MessageQueue.IdleHandler() {
                @Override
                public boolean queueIdle() {
                    if (sIdleInstallBundles.size() == 0 && sDelayInstallBundles.size() == 0) {
                        sIdleHandler = null;
                        return false;
                    }
                    Pair<String,InstallListener> bundleStruct = null;
                    if (sDelayInstallBundles.size() > 0) {
                        bundleStruct = sDelayInstallBundles.remove(0);
                    } else if (sIdleInstallBundles.size() > 0) {
                        bundleStruct = sIdleInstallBundles.remove(0);
                    }
                    if(bundleStruct!=null) {
                        String bundleName = bundleStruct.first;
                        InstallListener listener = bundleStruct.second;
                        BundleImpl impl = (BundleImpl) Atlas.getInstance().getBundle(bundleName);
                        if (impl == null || !impl.checkValidate()) {
                            Log.d("BundleInstaller", "idle install bundle : " + bundleName);
                            BundleInstallerFetcher.obtainInstaller().installTransitivelyAsync(new String[]{bundleName},listener);
                            return true;
                        }
                    }
                    return true;
                }
            };
            Looper.myQueue().addIdleHandler(sIdleHandler);
        }
    }

    public static void startIdleInstall(final String location,InstallListener listener){
        createIdleInstallerIfNeed();
        if(location!=null){
            BundleImpl impl = (BundleImpl)Atlas.getInstance().getBundle(location);
            if(impl==null || !impl.checkValidate()){
                sIdleInstallBundles.add(new Pair<String, InstallListener>(location,listener));
            }
        }

    }

    public static void startDelayInstall(final String location,InstallListener listener)
    {
        createIdleInstallerIfNeed();
        if(location!=null){
            BundleImpl impl = (BundleImpl)Atlas.getInstance().getBundle(location);
            if(impl==null || !impl.checkValidate()){
                sDelayInstallBundles.add(new Pair<String, InstallListener>(location,listener));
            }
        }
    }

