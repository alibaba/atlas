    void cleanup(final boolean full) {
    }

    /**
     * find a class. The following order has to be used for loading classes:
     * <ol>
     * <li>The system class loader (already done by the loadClass method prior to this method call)</li>
     * <li>The own bundle</li>
     * <li>Possibly imported packages</li>
     * </ol>
     *
     * @param classname the name of the class.
     * @return the <code>Class</code> object, if the class could be found.
     * @throws ClassNotFoundException if the class could not be found.
     * @category ClassLoader
     * @see java.lang.ClassLoader#findClass(java.lang.String)
     */
    protected Class<?> findClass(final String classname) throws ClassNotFoundException {

        Class<?> clazz;
        // okay, check if it is in the scope of this classloader
        clazz = findOwnClass(classname);
        if (clazz != null) {

            if(classLoadListener!=null){
                classLoadListener.onClassLoaded(clazz);
            }
            return clazz;
        }
        // find class in PathClassLoader
        try {
            clazz = Framework.systemClassLoader.loadClass(classname);
            if (clazz != null) {
                return clazz;
            }
        } catch (Exception e) {
        }

        // find class in dependency bundle
        if (dependencies != null) {
            for (String dependencyBundle : dependencies) {
                try {
                    BundleImpl impl = (BundleImpl) Atlas.getInstance().getBundle(dependencyBundle);
                    impl.startBundle();
                    String[] exports = ((BundleClassLoader) impl.getClassLoader()).getExports();
                    if (exports != null && exports.length > 0) {
                        //if has ,only loadclass when className in exports
                        for(String export : exports){
                            if(classname.startsWith(export)){
                                clazz = ((BundleClassLoader) impl.getClassLoader()).loadOwnClass(classname);
                                if(clazz!=null){
                                    return clazz;
                                }
                            }
                        }
                    } else {
                        clazz = ((BundleClassLoader) impl.getClassLoader()).loadOwnClass(classname);
                        if(clazz!=null){
                            return clazz;
                        }
                    }
                } catch (Throwable e) {
                    //e.printStackTrace();
                }
            }
        }

        throw new ClassNotFoundException("Can't find class " + classname + " in BundleClassLoader: " + bundle.getLocation());
    }

    /**
     * Find a class in the scope of this classloader.
     *
     * @param classname the name of the class.
     * @return the <code>Class</code> object if the class could be found. <code>null</code> otherwise.
     * @throws Exception
     */
    private Class<?> findOwnClass(final String classname) {
        try {
            Class<?> clazz = archive.findClass(classname, this);
            return clazz;
        } catch (Exception e) {
            if (e instanceof BundleArchiveRevision.DexLoadException) {
                throw (BundleArchiveRevision.DexLoadException) e;
            }
        }
        return null;
    }

    /**
     * Find a class that is delegated from a different classloader by package import.
     *
     * @param classname the name of the class.
     * @return the <code>Class</code> object, if the class could be found. <code>null</code> otherwise.
     */

    private static Class<?> findDelegatedClass(final BundleClassLoader delegation, final String classname) {
        final Class<?> clazz;
        synchronized (delegation) {
            return ((clazz = delegation.findLoadedClass(classname)) == null) ? delegation.findOwnClass(classname) : clazz;
        }
    }

    /**
     * find a single resource.
     *
     * @param filename the name of the resource.
     * @return the URL to the resource.
     * @category ClassLoader
     * @see java.lang.ClassLoader#findResource(java.lang.String)
     */
    protected URL findResource(final String filename) {
        final String name = stripTrailing(filename);
        List<URL> results = findOwnResources(name, false);
        if (results.size() > 0) {
            return results.get(0);
        }
        try {
            results = findImportedResources(name, false);
        }catch(NullPointerException e){
            return null;
        }
        return results.size() > 0 ? results.get(0) : null;
    }

    /**
     * find multiple resources.
     *
     * @param filename the name of the resource.
     * @return an <code>Enumeration</code> over <code>URL</code> objects.
     * @category ClassLoader
     * @see java.lang.ClassLoader#findResources(java.lang.String)
     */
    protected Enumeration<URL> findResources(final String filename) {
        final String name = stripTrailing(filename);
        final List<URL> results = findOwnResources(name, true);
        results.addAll(findImportedResources(name, true));
        return Collections.enumeration(results);
    }

    /**
     * find one or more resources in the scope of the own classloader.
     *
     * @param name     the name of the resource
     * @param multiple if false, the search terminates if the first result has been found.
     * @return a <code>List</code> of <code>URL</code> elements.
     */
    private List<URL> findOwnResources(final String name, boolean multiple) {
        try {
            return archive.getResources(name);
        } catch (IOException e) {
            e.printStackTrace();
        }
        return EMPTY_LIST;
    }

    /**
     * find one or more resources imported from other bundles.
     *
     * @param name     the name of the resource.
     * @param multiple if false, the searching terminates if the first resource has been found.
     * @return a <code>List</code> of <code>URL</code> elements.
     */
    private List<URL> findImportedResources(final String name, final boolean multiple) {
        if (bundle.state == Bundle.INSTALLED) {
            return EMPTY_LIST;
        }
        BundleClassLoader delegation = null;
        String packageName = packageOf(pseudoClassname(name));
        if (dependencies != null) {
            for (String dependencyBundle : dependencies) {
                BundleClassLoader loader = (BundleClassLoader)((BundleImpl)Atlas.getInstance().getBundle(dependencyBundle)).getClassLoader();
                String[] exports = loader.getExports();
                if (exports != null && exports.length > 0) {
                    for (String export : exports) {
                        if (packageName.startsWith(export)) {
                            delegation = loader;
                        }
                    }
                }
            }
        }

        if (delegation == null) {
            return EMPTY_LIST;
        } else {
            return delegation.findOwnResources(name, multiple);
        }
    }

    /**
     * find a native code library.
     *
     * @param libraryName the name of the library.
     * @return the String of a path name to the library or <code>null</code>.
     * @category ClassLoader
     * @see java.lang.ClassLoader#findLibrary(java.lang.String)
     */
    public String findLibrary(final String libraryName) {
        String fileName = System.mapLibraryName(libraryName);

        File soFile = archive.findLibrary(fileName);
        if (soFile != null) {
            return soFile.getAbsolutePath();
        }

        try {
            return (String) AtlasHacks.ClassLoader_findLibrary.invoke(Framework.systemClassLoader, libraryName);
        } catch (Exception e) {
            e.printStackTrace();
        }

        return null;
    }

    public void addRuntimeDependency(String bundleName){
        if(dependencies==null){
            dependencies = new ArrayList<String>();
        }
        if(!dependencies.contains(bundleName)){
            AtlasBundleInfoManager.instance().getBundleInfo(location).addRuntimeDependency(bundleName);
            dependencies.add(bundleName);
        }
    }

    /**
     * get a string representation of the object.
     *
     * @return a string.
     * @category Object
     * @see java.lang.Object#toString()
     */
    public String toString() {
        return "BundleClassLoader[Bundle" + bundle + "]";
    }

    /*
     * static methods
     */

    /**
     * read a property from the manifest attributes.
     *
     * @param attrs    the attributes.
     * @param property the name of the property to read.
     * @return the values.
     * @throws BundleException if the manifest has an empty value for the key.
     */
    private static String[] readProperty(final Attributes attrs, final String property) throws BundleException {
        final String values = attrs.getValue(property);
        if (values != null && values.equals("")) {
            // throw new BundleException("Broken manifest, " + property + " is empty.");
            return new String[0];
        }

        return splitString(values);
    }

    private static String[] splitString(final String values) {
        if (values == null) {
            return new String[0];
        }
        final StringTokenizer tokenizer = new StringTokenizer(values, ",");
        if (tokenizer.countTokens() == 0) {
            return new String[]{values};
        }
        final String[] result = new String[tokenizer.countTokens()];
        for (int i = 0; i < result.length; i++) {
            result[i] = tokenizer.nextToken().trim();
        }
        return result;
    }

    /**
     * strip trailing spaces.
     *
     * @param filename the file name.
     * @return the stripped file name.
     */
    private static String stripTrailing(final String filename) {
        return (filename.startsWith("/") || filename.startsWith("\\")) ? filename.substring(1) : filename;
    }

    /**
     * get the package of a class.
     *
     * @param classname
     * @return the package.
     */
    private static String packageOf(final String classname) {
        final int pos = classname.lastIndexOf('.');
        return pos > -1 ? classname.substring(0, pos) : "";
    }

    /**
     * create a pseudo classname from a file.
     *
     * @param filename the filename.
     * @return the pseudo classname.
     */
    private static String pseudoClassname(final String filename) {
        return stripTrailing(filename).replace('.', '-').replace('/', '.').replace('\\', '.');
    }

    public interface ClassLoadListener{
        public void onClassLoaded(Class clazz);
    }

