public class ActivityThreadHook implements Handler.Callback{

    private final Handler mActivityThreadHandler;
    private final Object  mActivityThread;
    private List<Message> sDelayServiceMessageList = null;

    public ActivityThreadHook(Object activityThread,Handler h){
        mActivityThread = activityThread;
        mActivityThreadHandler = h;
    }

    public void ensureLoadedApk() throws Exception{
        Object loadedapk = AndroidHack.getLoadedApk(RuntimeVariables.androidApplication,mActivityThread,RuntimeVariables.androidApplication.getPackageName());
        if(loadedapk==null){
//            AtlasMonitor.getInstance().trace(AtlasMonitor.LOADEDAPK_MISSING,"loaded apk","handle message","");
            ActivityTaskMgr.getInstance().clearActivityStack();
            android.os.Process.killProcess(android.os.Process.myPid());
        }
    }

    @Override
    public boolean handleMessage(Message msg) {
        try {
            ensureLoadedApk();
            if(msg.what == 100){
                ActivityBridge.processActivityIntentIfNeed(msg.obj);
            }
//            if(msg.what==113){
//                handleReceiver(msg);
//            }else if(msg.what == 114 && sDelayServiceMessageList==null){
//                handleService(msg);
//            }else if((msg.what==114 || msg.what ==115 || msg.what ==116 || msg.what == 121 || msg.what==122) && sDelayServiceMessageList!=null){
//                sDelayServiceMessageList.add(Message.obtain(msg));
//            }else {
                mActivityThreadHandler.handleMessage(msg);
//            }
            ensureLoadedApk();
        } catch (Throwable e) {
            e.printStackTrace();
            String appVersion = null;
            String avliableSpace = "";
            long filesSize = 0;
            long databasesSize = 0;
            long prefSize = 0;
            long rootSize = 0;
            try {
                if(RuntimeVariables.androidApplication!=null) {
                    appVersion = RuntimeVariables.androidApplication.getPackageManager().getPackageInfo(RuntimeVariables.androidApplication.getPackageName(), 0).versionName;
                }
            } catch (Throwable e1) {
//                e1.printStackTrace();
            }
            String error = getStackTrace(e);
            if(error!=null && error.contains("RemoteServiceException") && !Framework.isDeubgMode()){
                return true;
            }
            appVersion = (appVersion==null? "" : appVersion);
            if (e instanceof ClassNotFoundException || e.toString().contains("ClassNotFoundException")) {
                //RECEIVER 参见ActivityThread
                if(msg.what == 113 || msg.what==114) {
                    return true;
                }
                Object loadedapk = AndroidHack.getLoadedApk(RuntimeVariables.androidApplication,
                        mActivityThread,RuntimeVariables.androidApplication.getPackageName());
                if (loadedapk == null){
                    throw new RuntimeException(appVersion+"avalialbeSpace = " + avliableSpace  +
                    		"rootSize = " + rootSize + " filesSize = " + filesSize + " databasesSize =  " + databasesSize + " prefSize =" + prefSize+ "loadedapk is null");
                }
                ClassLoader pathClassLoader = AtlasHacks.LoadedApk_mClassLoader.get(loadedapk);
                if (!(pathClassLoader instanceof DelegateClassLoader)) {
                    throw new RuntimeException(appVersion+"avalialbeSpace = " + avliableSpace  + 
                    		"rootSize = " + rootSize + " filesSize = " + filesSize + " databasesSize =  " + databasesSize + " prefSize =" + prefSize + "wrong classloader in loadedapk---"+pathClassLoader.getClass().getName(),e);
                } else {
                    throw new RuntimeException(appVersion+"avalialbeSpace = " + avliableSpace  + 
                    		"rootSize = " + rootSize + " filesSize = " + filesSize + " databasesSize =  " + databasesSize + " prefSize =" + prefSize + "From Atlas:classNotFound ---", e);
                }
            }else if(e.toString().contains("android.content.res.Resources") && !e.toString().contains("OutOfMemoryError")){
                Object loadedapk = AndroidHack.getLoadedApk(RuntimeVariables.androidApplication,
                        mActivityThread,RuntimeVariables.androidApplication.getPackageName());
                if (loadedapk == null){
                    throw new RuntimeException(appVersion+ "loadedapk is null", e);
                }
                Resources resource = AtlasHacks.LoadedApk_mResources.get(loadedapk);
                if (!(resource instanceof DelegateResources)) {
                    throw new RuntimeException(appVersion+ "Not DelegateResources type, " + "DelegateResources paths are: "+ DelegateResources.getCurrentAssetpathStr(RuntimeVariables.androidApplication.getAssets()), e);
                }
                throw new RuntimeException(appVersion + "DelegateResources paths are: "+ DelegateResources.getCurrentAssetpathStr(RuntimeVariables.androidApplication.getAssets()), e);
            }else{
                throw new RuntimeException(appVersion, e);
            }
        }
        return true;

    }

    /**
     * 完整的堆栈信息
     *
     * @param e Exception
     * @return Full StackTrace
     */
    public static String getStackTrace(Throwable e) {
        StringWriter sw = null;
        PrintWriter pw = null;
        try {
            sw = new StringWriter();
            pw = new PrintWriter(sw);
            e.printStackTrace(pw);
            pw.flush();
            sw.flush();
        } finally {
            if (sw != null) {
                try {
                    sw.close();
                } catch (IOException e1) {
//                    e1.printStackTrace();
                }
            }
            if (pw != null) {
                pw.close();
            }
        }
        return sw.toString();
    }

    private void handleService(final Message message) throws Exception{
        Class ReceiverData = Class.forName("android.app.ActivityThread$CreateServiceData");
        final Field info_field = ReceiverData.getDeclaredField("info");
        info_field.setAccessible(true);
        final ServiceInfo info = (ServiceInfo)info_field.get(message.obj);
        String componentName = info.name;
        String bundleName = AtlasBundleInfoManager.instance().getBundleForComponet(componentName);
        if(!TextUtils.isEmpty(bundleName)){
            BundleImpl impl = (BundleImpl)Atlas.getInstance().getBundle(bundleName);
            if(impl!=null && impl.checkValidate()) {
                mActivityThreadHandler.handleMessage(message);
                if(sDelayServiceMessageList!=null){
                    sDelayServiceMessageList.remove(message);
                }
                executeDelayMsg();
            }else{
                if(sDelayServiceMessageList==null) {
                    sDelayServiceMessageList = new ArrayList<Message>();
                    sDelayServiceMessageList.add(Message.obtain(message));
                }
                BundleUtil.checkBundleStateAsync(bundleName, new Runnable() {
                    @Override
                    public void run() {
                        try {
                            executeDelayMsg();
                        }catch(Throwable e){throw new RuntimeException(e);}
                    }
                }, null);
            }
        }else{
            mActivityThreadHandler.handleMessage(message);
            if(sDelayServiceMessageList!=null){
                sDelayServiceMessageList.remove(message);
            }
            executeDelayMsg();
        }

    }

    private void executeDelayMsg() throws Exception{
        if(sDelayServiceMessageList!=null) {
            int size = sDelayServiceMessageList.size();
            for (int x = 0; x < size; x++) {
                Message msg = sDelayServiceMessageList.get(0);
                if (msg.what == 114) {
                    handleService(msg);
                    return;
                } else {
                    sDelayServiceMessageList.remove(msg);
                    mActivityThreadHandler.handleMessage(msg);
                    msg.recycle();
                }
            }
            sDelayServiceMessageList = null;
        }
    }
}
